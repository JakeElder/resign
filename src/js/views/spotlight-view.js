//==============================================================================
// Dependencies
//==============================================================================

var Backbone = require('backbone');
var $        = require('jquery');


//==============================================================================
// Constructor
//==============================================================================

var SpotlightView = function() {
  Backbone.View.apply(this, arguments);
};
$.extend(SpotlightView.prototype, Backbone.View.prototype, {
  className: 'spotlight'
});


//==============================================================================
// Export
//==============================================================================

module.exports = SpotlightView;

